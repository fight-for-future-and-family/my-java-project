package com.hoolai.bi.report.model.entity;

import java.util.Date;

public class ChannelCpa {
    private Long id;

    private String ds;

    private String snid;

    private String gameid;

    private String channelId;

    private String channelName;

    private Integer realActive;

    private Integer realRegist;

    private Double day1;

    private Double day3;

    private Double day7;

    private Date createTime;

    public ChannelCpa(String snid, String gameid) {
		this.snid = snid;
		this.gameid = gameid;
	}

	public ChannelCpa() {
	}

	public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getDs() {
        return ds;
    }

    public void setDs(String ds) {
        this.ds = ds;
    }

    public String getSnid() {
        return snid;
    }

    public void setSnid(String snid) {
        this.snid = snid;
    }

    public String getGameid() {
        return gameid;
    }

    public void setGameid(String gameid) {
        this.gameid = gameid;
    }

    public String getChannelId() {
        return channelId;
    }

    public void setChannelId(String channelId) {
        this.channelId = channelId;
    }

    public String getChannelName() {
        return channelName;
    }

    public void setChannelName(String channelName) {
        this.channelName = channelName;
    }

    public Integer getRealActive() {
        return realActive;
    }

    public void setRealActive(Integer realActive) {
        this.realActive = realActive;
    }

    public Integer getRealRegist() {
        return realRegist;
    }

    public void setRealRegist(Integer realRegist) {
        this.realRegist = realRegist;
    }

    public Double getDay1() {
        return day1;
    }

    public void setDay1(Double day1) {
        this.day1 = day1;
    }

    public Double getDay3() {
        return day3;
    }

    public void setDay3(Double day3) {
        this.day3 = day3;
    }

    public Double getDay7() {
        return day7;
    }

    public void setDay7(Double day7) {
        this.day7 = day7;
    }

    public Date getCreateTime() {
        return createTime;
    }

    public void setCreateTime(Date createTime) {
        this.createTime = createTime;
    }
}