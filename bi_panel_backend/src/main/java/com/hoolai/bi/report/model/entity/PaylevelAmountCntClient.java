package com.hoolai.bi.report.model.entity;

import java.util.Date;

public class PaylevelAmountCntClient {
    private Long id;

    private String snid;

    private String gameid;

    private String clientid;

    private Integer paylevel;

    private Integer payer;

    private Double totalPaymentAmount;

    private Integer totalPaymentCnt;

    private String statDay;

    private Date createTime;
    
    public PaylevelAmountCntClient(){}
    
    public PaylevelAmountCntClient(String snid,String gameid,String clientid){
    	this.snid = snid;
    	this.gameid = gameid;
    	this.clientid = clientid;
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getSnid() {
        return snid;
    }

    public void setSnid(String snid) {
        this.snid = snid;
    }

    public String getGameid() {
        return gameid;
    }

    public void setGameid(String gameid) {
        this.gameid = gameid;
    }

    public String getClientid() {
        return clientid;
    }

    public void setClientid(String clientid) {
        this.clientid = clientid;
    }

    public Integer getPaylevel() {
        return paylevel;
    }

    public void setPaylevel(Integer paylevel) {
        this.paylevel = paylevel;
    }

    public Integer getPayer() {
        return payer;
    }

    public void setPayer(Integer payer) {
        this.payer = payer;
    }

    public Double getTotalPaymentAmount() {
        return totalPaymentAmount;
    }

    public void setTotalPaymentAmount(Double totalPaymentAmount) {
        this.totalPaymentAmount = totalPaymentAmount;
    }

    public Integer getTotalPaymentCnt() {
        return totalPaymentCnt;
    }

    public void setTotalPaymentCnt(Integer totalPaymentCnt) {
        this.totalPaymentCnt = totalPaymentCnt;
    }

    public String getStatDay() {
        return statDay;
    }

    public void setStatDay(String statDay) {
        this.statDay = statDay;
    }

    public Date getCreateTime() {
        return createTime;
    }

    public void setCreateTime(Date createTime) {
        this.createTime = createTime;
    }
}