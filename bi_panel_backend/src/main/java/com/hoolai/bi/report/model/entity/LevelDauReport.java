package com.hoolai.bi.report.model.entity;

import java.util.Date;

public class LevelDauReport {
    private Long id;

    private String snid;

    private String gameid;

    private String ds;

    private Integer dauLevel;

    private Integer dau;

    private Integer totalDau;

    private Date createTime;
    
    public LevelDauReport(){}

    public LevelDauReport(String snid, String gameid) {
		this.snid = snid;
		this.gameid = gameid;
	}

	public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getSnid() {
        return snid;
    }

    public void setSnid(String snid) {
        this.snid = snid;
    }

    public String getGameid() {
        return gameid;
    }

    public void setGameid(String gameid) {
        this.gameid = gameid;
    }

    public String getDs() {
        return ds;
    }

    public void setDs(String ds) {
        this.ds = ds;
    }

    public Integer getDauLevel() {
        return dauLevel;
    }

    public void setDauLevel(Integer dauLevel) {
        this.dauLevel = dauLevel;
    }

    public Integer getDau() {
        return dau;
    }

    public void setDau(Integer dau) {
        this.dau = dau;
    }

    public Integer getTotalDau() {
        return totalDau;
    }

    public void setTotalDau(Integer totalDau) {
        this.totalDau = totalDau;
    }

    public Date getCreateTime() {
        return createTime;
    }

    public void setCreateTime(Date createTime) {
        this.createTime = createTime;
    }
}