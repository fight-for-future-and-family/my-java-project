package com.hoolai.bi.report.model.entity;

public class RealTimeResult {
    private Integer id;

    private String snid;

    private String clientid;

    private String ds;

    private String gameid;

    private Integer dau;

    private Integer install;

    private Integer payment;

    private Integer paytimes;

    private Float amount;
    
    public RealTimeResult(){}

    public RealTimeResult(String snid, String gameid) {
		this.snid = snid;
		this.gameid = gameid;
	}

	public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getSnid() {
        return snid;
    }

    public void setSnid(String snid) {
        this.snid = snid;
    }

    public String getClientid() {
        return clientid;
    }

    public void setClientid(String clientid) {
        this.clientid = clientid;
    }

    public String getDs() {
        return ds;
    }

    public void setDs(String ds) {
        this.ds = ds;
    }

    public String getGameid() {
        return gameid;
    }

    public void setGameid(String gameid) {
        this.gameid = gameid;
    }

    public Integer getDau() {
        return dau;
    }

    public void setDau(Integer dau) {
        this.dau = dau;
    }

    public Integer getInstall() {
        return install;
    }

    public void setInstall(Integer install) {
        this.install = install;
    }

    public Integer getPayment() {
        return payment;
    }

    public void setPayment(Integer payment) {
        this.payment = payment;
    }

    public Integer getPaytimes() {
        return paytimes;
    }

    public void setPaytimes(Integer paytimes) {
        this.paytimes = paytimes;
    }

    public Float getAmount() {
        return amount;
    }

    public void setAmount(Float amount) {
        this.amount = amount;
    }
}