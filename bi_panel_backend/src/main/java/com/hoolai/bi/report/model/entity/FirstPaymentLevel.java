package com.hoolai.bi.report.model.entity;

import java.util.Date;

public class FirstPaymentLevel {
    private Long id;

    private String snid;

    private String gameid;

    private String statDay;

    private Integer level;

    private Integer payerNum;

    private Double totalAmount;

    private Date createTime;
    
    public FirstPaymentLevel(){}
    
    public FirstPaymentLevel(String snid,String gameid){
    	this.snid = snid;
    	this.gameid = gameid;
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getSnid() {
        return snid;
    }

    public void setSnid(String snid) {
        this.snid = snid;
    }

    public String getGameid() {
        return gameid;
    }

    public void setGameid(String gameid) {
        this.gameid = gameid;
    }

    public String getStatDay() {
        return statDay;
    }

    public void setStatDay(String statDay) {
        this.statDay = statDay;
    }

    public Integer getLevel() {
        return level;
    }

    public void setLevel(Integer level) {
        this.level = level;
    }

    public Integer getPayerNum() {
        return payerNum;
    }

    public void setPayerNum(Integer payerNum) {
        this.payerNum = payerNum;
    }

    public Double getTotalAmount() {
        return totalAmount;
    }

    public void setTotalAmount(Double totalAmount) {
        this.totalAmount = totalAmount;
    }

    public Date getCreateTime() {
        return createTime;
    }

    public void setCreateTime(Date createTime) {
        this.createTime = createTime;
    }
}